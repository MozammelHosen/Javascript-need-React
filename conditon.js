/*
1. Equal value ===
2. Not Equal    !==
3. Greater Than  >
4. less Than <
5. Greater Than Or Equal to >=
6. Less Than Or Equal to <=
*/


let money = 5000;

// equal Value
if (money === 5000) {
  console.log("Money Is 5000");
} else {
  console.log("Money Is Not Equal In 5000");
}
// Not Equal Value
if (money !== 5000) {
  console.log("Money is Not Equal");
} else {
  console.log("Money is Equal");
}
// greater than
if (money > 5000) {
  console.log("Money is Greater Than 5000");
} else {
  console.log("Money is Not Greater Than 5000");
}
// less Than
if (money < 5000) {
  console.log("Money is Less than 5000");
} else {
  console.log("Money is Not less than 5000");
}

// greater Than or qual to
if (money >= 5000) {
    console.log("Money is Greater Than or Equal 5000");
  } else {
    console.log("Money is Not Greater Than 5000");
  }
// Less Than or qual to
if (money >= 5000) {
    console.log("Money is Less Than or Equal 5000");
  } else {
    console.log("Money is Not Less Than 5000");
  }